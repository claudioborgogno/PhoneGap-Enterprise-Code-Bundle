TRIGGER BI_PERSON_SEQ
BEFORE INSERT ON PERSON
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
BEGIN
  IF :new.ID IS NULL THEN
    :new.ID := PERSON_SEQ.nextval();
  END IF;
END;

